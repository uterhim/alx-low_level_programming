#include "3-calc.h"
/**
 * op_add - perform summation
 * @a: first variable
 * @b: second variable
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - perform summation
 * @a: first variable
 * @b: second variable
 * Return: return difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - perform product
 * @a: first variable
 * @b: second variable
 * Return: product of two number
 */
int op_muld(int a, int b)
{
	return (a * b);
}

/**
 * op_div - perform division
 * @a: first variable
 * @b: second variable
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - perform module
 * @a: first variable
 * @b: second variable
 * Return: module result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
